func TestParseResolvConf(t *testing.T) {
	tests := []struct {
		name    string
		content string
		want    []string
	}{
		{
			"standard two nameservers",
			"# Generated by NetworkManager\nnameserver 8.8.8.8\nnameserver 8.8.4.4\nsearch example.com\n",
			[]string{"8.8.8.8", "8.8.4.4"},
		},
		{
			"comments and empty lines",
			"# comment\n\nnameserver 1.1.1.1\n# another comment\nnameserver 9.9.9.9\n\n",
			[]string{"1.1.1.1", "9.9.9.9"},
		},
		{
			"empty content",
			"",
			nil,
		},
		{
			"no nameserver lines",
			"# just comments\nsearch example.com\ndomain example.com\n",
			nil,
		},
		{
			"trailing whitespace",
			"nameserver 8.8.8.8  \nnameserver 8.8.4.4\t\n",
			[]string{"8.8.8.8", "8.8.4.4"},
		},
		{
			"ipv6 nameserver",
			"nameserver 2001:4860:4860::8888\nnameserver 8.8.8.8\n",
			[]string{"2001:4860:4860::8888", "8.8.8.8"},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := parseResolvConf(tt.content)
			if len(got) != len(tt.want) {
				t.Fatalf("parseResolvConf() returned %d entries, want %d\ngot:  %v\nwant: %v",
					len(got), len(tt.want), got, tt.want)
			}
			for i := range got {
				if got[i] != tt.want[i] {
					t.Errorf("entry[%d] = %q, want %q", i, got[i], tt.want[i])
				}
			}
		})
	}
}

