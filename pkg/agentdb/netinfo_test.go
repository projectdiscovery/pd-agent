func TestClassifyIP(t *testing.T) {
	tests := []struct {
		name    string
		ip      net.IP
		private bool
	}{
		// Private/reserved IPs — should return true
		{"rfc1918 10.x", net.ParseIP("10.0.0.1"), true},
		{"rfc1918 172.16.x", net.ParseIP("172.16.0.1"), true},
		{"rfc1918 192.168.x", net.ParseIP("192.168.1.1"), true},
		{"rfc6598 cgnat", net.ParseIP("100.64.0.1"), true},
		{"loopback", net.ParseIP("127.0.0.1"), true},
		{"link-local", net.ParseIP("169.254.1.1"), true},
		{"nil ip", nil, true},
		{"ipv6 ULA", net.ParseIP("fd00::1"), true},

		// Public IPs — should return false
		{"google dns", net.ParseIP("8.8.8.8"), false},
		{"cloudflare dns", net.ParseIP("1.1.1.1"), false},
		{"test-net-3", net.ParseIP("203.0.113.1"), false},
		{"ipv6 public", net.ParseIP("2001:db8::1"), false},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := classifyIP(tt.ip)
			if got != tt.private {
				t.Errorf("classifyIP(%v) = %v, want %v", tt.ip, got, tt.private)
			}
		})
	}
}

